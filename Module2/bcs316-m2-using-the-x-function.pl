#Example of using the "x" to display multiple columns of the same data.
# the script displays one line with 10 characters, the next with 20 characters and the last with 40 characters.
$str= "x" x 10;
use 5.28.0;
print $str . "\n";
$str1= "x" x 20;
print $str1 . "\n";
$str2= "x" x 40;
print $str2 . "\n";
